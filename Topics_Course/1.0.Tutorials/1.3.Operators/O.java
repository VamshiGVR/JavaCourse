public class O {
    public static void main(String[] args) {
        int a = 2;
        int b = 4;

        //OPERATORS
        int Add = a + b;
        int Sub = b - a;
        int Div = b/a;
        int Mult = a * b;
        int Mod = a % b;

        System.out.println("Add " + Add);
        System.out.println("Sub " + Sub);
        System.out.println("Mult "+Mult);
        System.out.println("Div " + Div);
        System.out.println("Mod "+ Mod);
    }
}
